using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.SceneManagement;

public class end_menu : MonoBehaviour
{
	private void Update()
	{
        Debug.Log("end");
        if (Input.GetKeyDown(KeyCode.S) || Input.GetKeyDown(KeyCode.DownArrow))
        {
            Debug.Log("text = " + transform.Find("exit").name);
            transform.Find("exit_txt").GetComponent<UnityEngine.UI.Text>().fontSize = 40;
            transform.Find("retry_txt").GetComponent<UnityEngine.UI.Text>().fontSize = 30;
        }
        else if (Input.GetKeyDown(KeyCode.W) || Input.GetKeyDown(KeyCode.UpArrow))
        {
            transform.Find("exit_txt").GetComponent<UnityEngine.UI.Text>().fontSize = 30;
            transform.Find("retry_txt").GetComponent<UnityEngine.UI.Text>().fontSize = 40;
        }
	}
	public void replay()
    {
        SceneManager.LoadScene(1);
    }

    public void exit()
    {
        Application.Quit();
    }
}
